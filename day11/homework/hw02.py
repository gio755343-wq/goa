# 2) შექმენით 8 ცვლადი ლოგიკური ოპერატორების მაგალითებით (4 ცალი OR 4 ცალი AND)


or1 = 10 > 5 or 3 > 7
or2 = 5 == 5 or 2 == 3
or3 = False or True
or4 = 8 < 3 or 4 <= 4


and1 = 10 > 5 and 3 < 7
and2 = 5 == 5 and 2 == 2
and3 = True and False
and4 = 8 >= 8 and 4 < 10

print(or1, or2, or3, or4)
print(and1, and2, and3, and4)
