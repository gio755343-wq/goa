# 1)კომენტარის სახით ახსენით განსხვავება explicit და implicit data conversion შორის და დაწერე მაგალითები

# Implicit data conversion (ავტომატური გარდაქმნა) ხდება მაშინ, როცა Python თვითონ ცვლის მონაცემის ტიპს შეცდომის გარეშე

# Explicit data conversion (ხელით გარდაქმნა) ხდება მაშინ, როცა პროგრამისტი სპეციალურად იყენებს ფუნქციას ტიპის შესაცვლელად

